function pickForWorkflow({ requirement, categoriesForStep, budgetMode, excludedIds }) {
  const pool = aiTools
    .filter((tool) => categoriesForStep.includes(tool.category))
    .filter((tool) => isToolAllowedForBudget(tool, budgetMode))
    .filter((tool) => !excludedIds.has(tool.id))
    .map((tool) => ({
      ...tool,
      recommendationScore: calculateRecommendationScore(tool, requirement.toLowerCase().split(/\s+/).filter(Boolean), requirement.toLowerCase(), 'All', budgetMode),
      utilityScore: getUtilityScore(tool),
    }))
    .sort((a, b) => b.recommendationScore - a.recommendationScore);

  const primary = pool[0] || null;
  let alternative = pool[1] || null;

  if (primary && getPricingMode(primary.pricing) === 'paid') {
    const freeAlternative = pool.find((candidate) => candidate.id !== primary.id && getPricingMode(candidate.pricing) !== 'paid');
    if (freeAlternative) alternative = freeAlternative;
  }

  return {
    primary,
    alternative,
  };
}

export function recommendWorkflow(requirement, category = 'All', options = {}) {
  const budgetMode = inferBudgetMode(requirement, options);
  const template = getWorkflowTemplate(requirement);
  const excludedIds = new Set();

  const steps = template.steps
    .map((step) => {
      const categoriesForStep = category !== 'All' ? [category] : step.categories;
      const { primary, alternative } = pickForWorkflow({
        requirement,
        categoriesForStep,
        budgetMode,
        excludedIds,
      });

      if (primary) excludedIds.add(primary.id);

      return {
        role: step.role,
        categories: categoriesForStep,
        primary,
        alternative,
      };
    })
    .filter((step) => step.primary);

  return {
    budgetMode,
    template: template.id,
    steps,
  };
}

export function getTopTools(count = 10, sortBy = 'score') {
  if (sortBy === 'utility') {
    return [...aiTools]
      .map((tool) => ({ ...tool, utilityScore: getUtilityScore(tool) }))
      .sort((a, b) => b.utilityScore - a.utilityScore)
      .slice(0, count);
  }

  return [...aiTools].sort((a, b) => b.score - a.score).slice(0, count);
}

export function getTrendingTools() {
  return aiTools.filter((t) => t.trending).sort((a, b) => b.score - a.score);
}

export function getToolsByCategory(category) {
  if (category === 'All') return [...aiTools];
  return aiTools.filter((t) => t.category === category);
}

export function getScoreColor(score) {
  if (score >= 90) return '#22c55e';
  if (score >= 80) return '#eab308';
  if (score >= 70) return '#f97316';
  return '#ef4444';
}
