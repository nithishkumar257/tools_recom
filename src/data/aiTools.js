function getPricingMode(pricing = '') {
  const normalized = pricing.toLowerCase();
  if (normalized.includes('free') && normalized.includes('paid')) return 'hybrid';
  if (normalized.includes('free') || normalized.includes('freemium')) return 'free';
  return 'paid';
}

function inferBudgetMode(requirement = '', options = {}) {
  if (options?.budgetMode) return options.budgetMode;
  const normalized = requirement.toLowerCase();
  if (/free|no\s?cost|zero\s?cost|cheap|student|budget|open\s?source/.test(normalized)) return 'free';
  if (/premium|enterprise|best\s?quality|highest\s?quality|pro\s?plan/.test(normalized)) return 'premium';
  return 'mixed';
}

function isToolAllowedForBudget(tool, budgetMode) {
  const pricingMode = getPricingMode(tool.pricing);
  if (budgetMode === 'free') return pricingMode === 'free' || pricingMode === 'hybrid';
  return true;
}

function getFreeAlternative(tool, pool) {
  if (getPricingMode(tool.pricing) !== 'paid') return null;

  return (
    pool
      .filter((candidate) => candidate.id !== tool.id)
      .filter((candidate) => candidate.category === tool.category)
      .filter((candidate) => getPricingMode(candidate.pricing) !== 'paid')
      .sort((a, b) => b.recommendationScore - a.recommendationScore)[0] || null
  );
}

function getKeywordMatches(words, tags) {
  return tags.reduce((acc, tag) => {
    return words.some((word) => tag.includes(word) || word.includes(tag)) ? acc + 1 : acc;
  }, 0);
}

export function getUtilityScore(tool) {
  const breakdown = tool.scoreBreakdown || {};
  const accuracy = breakdown.accuracy ?? tool.score;
  const speed = breakdown.speed ?? tool.score;
  const ease = breakdown.ease ?? tool.score;
  const value = breakdown.value ?? tool.score;
  const utility = accuracy * 0.35 + speed * 0.2 + ease * 0.2 + value * 0.25;
  return Math.round(utility);
}

function buildResearchMetadata(tool) {
  return {
    metadata_schema_version: '1.0.0',
    benchmark_profile: {
      source: 'app-local-heuristic',
      last_run_at: tool.lastUpdated,
    },
    quality_dimensions: {
      latency: tool.scoreBreakdown?.speed ?? tool.score,
      consistency: tool.scoreBreakdown?.accuracy ?? tool.score,
      usability: tool.scoreBreakdown?.ease ?? tool.score,
      cost_efficiency: tool.scoreBreakdown?.value ?? tool.score,
    },
    curation_signal_breakdown: {
      utility_score: getUtilityScore(tool),
      popularity_score: tool.score,
      trending: Boolean(tool.trending),
    },
    sponsorship_flag: false,
  };
}

function calculateRecommendationScore(tool, words, normalizedRequirement, category, budgetMode) {
  const keywordMatches = getKeywordMatches(words, tool.tags || []);
  const beginnerBoost = normalizedRequirement.includes('beginner') && tool.tags?.includes('beginner') ? 4 : 0;
  const categoryBoost = category !== 'All' && tool.category === category ? 5 : 0;
  const freeBoost = budgetMode === 'free' && getPricingMode(tool.pricing) !== 'paid' ? 8 : 0;
  const mixedModeBoost = budgetMode === 'mixed' && getPricingMode(tool.pricing) === 'hybrid' ? 4 : 0;
  const premiumBoost = budgetMode === 'premium' && getPricingMode(tool.pricing) === 'paid' ? 6 : 0;
  const trendingBoost = tool.trending ? 3 : 0;
  const utilityBoost = Math.round(getUtilityScore(tool) * 0.2);

  return tool.score + keywordMatches * 6 + beginnerBoost + categoryBoost + freeBoost + mixedModeBoost + premiumBoost + trendingBoost + utilityBoost;
}

export function recommendTools(requirement, category = 'All', max = 5, options = {}) {
  const normalized = requirement.toLowerCase().trim();
  const words = normalized.split(/\s+/).filter(Boolean);
  const budgetMode = inferBudgetMode(requirement, options);

  const scored = aiTools
    .filter((tool) => category === 'All' || tool.category === category)
    .filter((tool) => isToolAllowedForBudget(tool, budgetMode))
    .map((tool) => {
      const recommendationScore = calculateRecommendationScore(tool, words, normalized, category, budgetMode);
      return {
        ...tool,
        budgetMode,
        utilityScore: getUtilityScore(tool),
        recommendationScore,
        researchMetadata: buildResearchMetadata(tool),
      };
    })
    .sort((a, b) => b.recommendationScore - a.recommendationScore);

  return scored
    .map((tool) => {
      const freeAlternative = getFreeAlternative(tool, scored);

      return {
        ...tool,
        freeAlternative: freeAlternative
          ? {
              id: freeAlternative.id,
              name: freeAlternative.name,
              pricing: freeAlternative.pricing,
            }
          : null,
      };
    })
    .slice(0, max);
}

